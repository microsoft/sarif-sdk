using System;
using System.Collections.Generic;
using System.Globalization;
using System.IO;
using System.Reflection;
using System.Text;

using Newtonsoft.Json;

namespace Microsoft.CodeAnalysis.Sarif.Taxonomies
{
    /// <summary>
    /// Provides a stable, hand-curated <c>security-severity</c> prior for each MITRE CWE,
    /// on the 0.0–10.0 CVSS-aligned scale that both GitHub Advanced Security and Azure DevOps
    /// Advanced Security read off a rule descriptor to bucket a result into
    /// critical/high/medium/low.
    /// </summary>
    /// <remarks>
    /// <para>
    /// This is a per-rule <em>severity</em> prior — the expected seriousness of <em>any</em>
    /// finding of a given weakness class — and is orthogonal to <see cref="Result.Rank"/>, which
    /// carries a result's priority (and, under a least-likely-false-positive prioritization, its
    /// confidence). The two axes are deliberately kept distinct; one is never derived from the other.
    /// </para>
    /// <para>
    /// The table is the SDK's curated judgment, not an autogenerated projection. Its initial values
    /// were seeded from the published <c>@security-severity</c> metadata in the CodeQL query corpus
    /// (the median value observed across the queries tagged with each CWE, which lands on the
    /// canonical per-query value for narrowly-scoped CWEs and a defensible center for broad ones),
    /// then maintained by hand. To change a value, edit
    /// <c>src/Sarif/Taxonomies/CweSecuritySeverity.json</c> directly; there is no generator.
    /// </para>
    /// <para>
    /// A CWE absent from the table has no prior: consumers elide <c>security-severity</c> rather
    /// than stamp a default, and platforms degrade gracefully to their level-based severity.
    /// </para>
    /// </remarks>
    public static class CweSecuritySeverity
    {
        /// <summary>
        /// The property-bag key GitHub and Azure DevOps Advanced Security read a severity prior
        /// from, on both a rule descriptor and a CWE taxon: <c>security-severity</c>.
        /// </summary>
        public const string PropertyName = "security-severity";

        private const string ResourceName = "Microsoft.CodeAnalysis.Sarif.Taxonomies.CweSecuritySeverity.json";

        private static readonly object Gate = new object();
        private static IReadOnlyDictionary<int, double> securitySeverityByCwe;

        /// <summary>
        /// The full curated map, keyed by CWE number (e.g. <c>89</c>) to its
        /// <c>security-severity</c> value on the 0.0–10.0 scale.
        /// </summary>
        public static IReadOnlyDictionary<int, double> SecuritySeverityByCwe => LoadTable();

        /// <summary>
        /// Looks up the curated <c>security-severity</c> for a CWE number.
        /// </summary>
        /// <param name="cweNumber">The CWE number (e.g. <c>89</c>).</param>
        /// <param name="securitySeverity">The curated value on the 0.0–10.0 scale, when found.</param>
        /// <returns><c>true</c> when the CWE has a curated value; otherwise <c>false</c>.</returns>
        public static bool TryGetSecuritySeverity(int cweNumber, out double securitySeverity)
        {
            return LoadTable().TryGetValue(cweNumber, out securitySeverity);
        }

        /// <summary>
        /// Looks up the curated <c>security-severity</c> for a CWE identifier in any of the forms
        /// the SDK accepts: a bare number (<c>89</c>), a canonical id (<c>CWE-89</c>, any case,
        /// leading zeros tolerated), or an AI ruleId carrying a sub-id (<c>CWE-89/kql-injection</c>).
        /// </summary>
        /// <param name="cweId">The CWE identifier or AI ruleId to resolve.</param>
        /// <param name="securitySeverity">The curated value on the 0.0–10.0 scale, when found.</param>
        /// <returns>
        /// <c>true</c> when <paramref name="cweId"/> resolves to a CWE that has a curated value;
        /// otherwise <c>false</c> (including for the <c>NOVEL-</c> form, which carries no CWE).
        /// </returns>
        public static bool TryGetSecuritySeverity(string cweId, out double securitySeverity)
        {
            securitySeverity = default;
            return TryGetCweNumber(cweId, out int cweNumber)
                && TryGetSecuritySeverity(cweNumber, out securitySeverity);
        }

        /// <summary>
        /// Formats a <c>security-severity</c> value the way GitHub and Azure DevOps expect to read
        /// it off a rule descriptor: a fixed one-decimal, invariant-culture string (e.g. <c>"8.8"</c>).
        /// </summary>
        /// <param name="securitySeverity">The value on the 0.0–10.0 scale.</param>
        /// <returns>The formatted property-value string.</returns>
        public static string FormatPropertyValue(double securitySeverity)
        {
            return securitySeverity.ToString("0.0", CultureInfo.InvariantCulture);
        }

        /// <summary>
        /// Parses the CWE number out of any identifier the SDK accepts: a bare number
        /// (<c>89</c>), a canonical id (<c>CWE-89</c>, any case, leading zeros tolerated), or an
        /// AI ruleId carrying a sub-id (<c>CWE-89/kql-injection</c>). The <c>NOVEL-</c> form and
        /// anything non-conforming yield <c>false</c>.
        /// </summary>
        /// <param name="cweId">The CWE identifier or AI ruleId to resolve.</param>
        /// <param name="cweNumber">The parsed CWE number (e.g. <c>89</c>), when found.</param>
        /// <returns><c>true</c> when a CWE number was parsed; otherwise <c>false</c>.</returns>
        public static bool TryGetCweNumber(string cweId, out int cweNumber)
        {
            cweNumber = default;
            if (string.IsNullOrWhiteSpace(cweId)) { return false; }

            string token = cweId.Trim();

            int slash = token.IndexOf('/');
            if (slash >= 0) { token = token.Substring(0, slash); }

            if (token.StartsWith("CWE-", StringComparison.OrdinalIgnoreCase))
            {
                token = token.Substring(4);
            }

            return int.TryParse(token, NumberStyles.None, CultureInfo.InvariantCulture, out cweNumber);
        }

        private static IReadOnlyDictionary<int, double> LoadTable()
        {
            lock (Gate)
            {
                if (securitySeverityByCwe == null)
                {
                    securitySeverityByCwe = ParseEmbeddedTable();
                }
                return securitySeverityByCwe;
            }
        }

        private static IReadOnlyDictionary<int, double> ParseEmbeddedTable()
        {
            Assembly assembly = typeof(CweSecuritySeverity).Assembly;
            using Stream stream = assembly.GetManifestResourceStream(ResourceName)
                ?? throw new InvalidOperationException(
                    "Embedded CWE security-severity resource '" + ResourceName + "' was not found in assembly '"
                    + assembly.FullName + "'.");
            using var reader = new StreamReader(stream, Encoding.UTF8);

            var raw = JsonConvert.DeserializeObject<Dictionary<string, double>>(reader.ReadToEnd());
            var table = new Dictionary<int, double>(raw.Count);
            foreach (KeyValuePair<string, double> entry in raw)
            {
                if (TryGetCweNumber(entry.Key, out int cweNumber))
                {
                    table[cweNumber] = entry.Value;
                }
            }
            return table;
        }
    }
}
