/**
 * Canonical key ordering for byte-identical parity with the .NET multitool.
 *
 * The .NET multitool serializes through its typed object model, so its on-disk
 * key order is the model's DataMember order — independent of the order keys
 * arrived in. A JS object, by contrast, serializes in insertion order. To match
 * .NET byte-for-byte we reorder every typed node to the model order.
 *
 * The order + type graph live in dist/serialization-model.json, generated at
 * build time from the same authorities that define .NET's behavior:
 *   - key order: src/Sarif/Autogenerated/*.cs DataMember attributes
 *   - prop -> child-type graph: the published SARIF schema's $ref edges
 * See scripts/generate-serialization-model.mjs.
 *
 * Property bags (`@propertyBag`) are passed through untouched: their key order
 * is producer-determined and preserved by both runtimes.
 */

import { readFileSync } from 'node:fs';

const PROPERTY_BAG = '@propertyBag';

type ModelEntry = { order: string[]; children: Record<string, string> };
type SerializationModel = Record<string, ModelEntry>;

let modelCache: SerializationModel | undefined;

function model(): SerializationModel {
  if (modelCache === undefined) {
    const url = new URL('./serialization-model.json', import.meta.url);
    modelCache = JSON.parse(readFileSync(url, 'utf8')) as SerializationModel;
  }
  return modelCache;
}

function orderValue(value: unknown, def: string | undefined): unknown {
  if (Array.isArray(value)) {
    return value.map((v) => orderValue(v, def));
  }
  if (value !== null && typeof value === 'object') {
    if (!def || def === PROPERTY_BAG) return value; // property bag / untyped: preserve insertion order
    const entry = model()[def];
    if (!entry) return value;
    return orderObject(value as Record<string, unknown>, entry);
  }
  return value;
}

function orderObject(value: Record<string, unknown>, entry: ModelEntry): Record<string, unknown> {
  const out: Record<string, unknown> = {};
  for (const prop of entry.order) {
    if (Object.prototype.hasOwnProperty.call(value, prop)) {
      out[prop] = orderValue(value[prop], entry.children[prop]);
    }
  }
  // Any keys the model doesn't know about (defensive; the typed model normally
  // covers everything) keep their relative insertion order at the tail.
  for (const key of Object.keys(value)) {
    if (!Object.prototype.hasOwnProperty.call(out, key)) {
      out[key] = value[key];
    }
  }
  return out;
}

/**
 * Returns a deep copy of `log` with every typed object's keys reordered to the
 * .NET model order. The input is not mutated.
 */
export function canonicalizeSarifLog<T>(log: T): T {
  return orderValue(log, 'sarifLog') as T;
}

// ---------------------------------------------------------------------------
// Canonical JSON emitter
// ---------------------------------------------------------------------------
//
// Newtonsoft (the .NET multitool's writer) serializes a SARIF log indented, but
// emits property-bag *values* verbatim-compact: they are stored as
// pre-serialized JToken text, so an array like `tags` lands on a single line
// (`"tags": ["security","external/cwe/cwe-89"]`) while the bag's keys stay
// indented. Typed arrays everywhere else are multi-line as usual. JSON.stringify
// cannot produce this mixed shape, so we emit it ourselves. This reorders to the
// model order in the same pass, so callers need not pre-canonicalize.

const INDENT = '  ';

function pad(depth: number): string {
  return INDENT.repeat(depth);
}

/** Emits a property bag: indented keys, but each value rendered verbatim-compact. */
function emitPropertyBag(value: Record<string, unknown>, depth: number): string {
  const keys = Object.keys(value);
  if (keys.length === 0) return '{}';
  const inner = pad(depth + 1);
  const lines = keys.map((k) => `${inner}${JSON.stringify(k)}: ${JSON.stringify(value[k])}`);
  return `{\n${lines.join(',\n')}\n${pad(depth)}}`;
}

function emitValue(value: unknown, def: string | undefined, depth: number): string {
  if (Array.isArray(value)) {
    if (value.length === 0) return '[]';
    const inner = pad(depth + 1);
    const lines = value.map((v) => `${inner}${emitValue(v, def, depth + 1)}`);
    return `[\n${lines.join(',\n')}\n${pad(depth)}]`;
  }
  if (value !== null && typeof value === 'object') {
    if (def === PROPERTY_BAG) return emitPropertyBag(value as Record<string, unknown>, depth);
    const obj = value as Record<string, unknown>;
    const entry = def ? model()[def] : undefined;
    const keys = orderedKeys(obj, entry);
    if (keys.length === 0) return '{}';
    const inner = pad(depth + 1);
    const lines = keys.map(
      (k) => `${inner}${JSON.stringify(k)}: ${emitValue(obj[k], entry?.children[k], depth + 1)}`,
    );
    return `{\n${lines.join(',\n')}\n${pad(depth)}}`;
  }
  return JSON.stringify(value);
}

/** Keys present on `obj`, in model order; unknown keys (defensive) trail in insertion order. */
function orderedKeys(obj: Record<string, unknown>, entry: ModelEntry | undefined): string[] {
  if (!entry) return Object.keys(obj);
  const seen = new Set<string>();
  const out: string[] = [];
  for (const prop of entry.order) {
    if (Object.prototype.hasOwnProperty.call(obj, prop)) {
      out.push(prop);
      seen.add(prop);
    }
  }
  for (const key of Object.keys(obj)) {
    if (!seen.has(key)) out.push(key);
  }
  return out;
}

/**
 * Serializes a SARIF log to the .NET multitool's byte shape: model-ordered keys,
 * two-space indent, and verbatim-compact property-bag values. `prettyPrint=false`
 * yields fully compact output (every value on one line), matching the .NET
 * minified writer. Uses LF newlines; the caller applies the platform newline.
 */
export function emitCanonicalJson(log: unknown, prettyPrint: boolean): string {
  if (!prettyPrint) return JSON.stringify(canonicalizeSarifLog(log));
  return emitValue(log, 'sarifLog', 0);
}
